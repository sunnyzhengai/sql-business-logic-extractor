CREATE VIEW SCHEMA.VIEWNAME AS

WITH CLM AS (
   SELECT C1, C2, C3
   FROM T1
     INNER JOIN T2 AS ALIAS2 ON T1.C1 = ALIAS2.C2
     INNER JOIN T3 AS ALIAS3 ON T2.C4 = ALIAS3.C5 AND ALIAS3.C6 = 32
   WHERE T1.STATUS = 1
   GROUP BY C1, C2, C3
),
CLMBASE AS (
    SELECT D1, D2 FROM CLM
)
SELECT C1, C2, C3 FROM CLMBASE
