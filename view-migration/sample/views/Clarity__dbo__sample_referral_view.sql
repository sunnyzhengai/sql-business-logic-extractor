CREATE VIEW dbo.sample_referral_view AS
WITH ActiveReferrals AS (
    SELECT
        R.REFERRAL_ID,
        R.PATIENT_ID,
        R.STATUS_C
    FROM Clarity.dbo.REFERRAL R
    WHERE R.STATUS_C IN (1, 2, 3)
)
SELECT
    AR.REFERRAL_ID,
    AR.PATIENT_ID,
    P.PAT_NAME,
    P.BIRTH_DATE,
    AUTH.AUTH_NUMBER,
    HSP.ADMIT_DATE
FROM ActiveReferrals AR
INNER JOIN Clarity.dbo.PATIENT P
    ON P.PAT_ID = AR.PATIENT_ID
LEFT JOIN CookClarity.Reporting.V_AUTH_FACT AUTH
    ON AUTH.REFERRAL_ID = AR.REFERRAL_ID
LEFT JOIN dbo.HSP_ACCOUNT HSP
    ON HSP.PAT_ID = AR.PATIENT_ID
WHERE EXISTS (
    SELECT 1
    FROM Clarity.dbo.REFERRAL_HIST RH
    WHERE RH.REFERRAL_ID = AR.REFERRAL_ID
      AND RH.NEW_STATUS_C = 5
);
